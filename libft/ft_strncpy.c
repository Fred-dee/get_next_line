#include "libft.h"
#include <stdlib.h>

char	*ft_strncpy(char *s1, const char *s2, size_t n)
{
	size_t	i;

	i = 0;
	while (s2[i] != '\0' && i < n)
	{
		s1[i] = s2[i];
		i++;
	}
	if (s2[i] == '\0')
		s1[i] = '\0';
	while (i < n)
		s1[i++] = '\0';
	return (s1);
}
