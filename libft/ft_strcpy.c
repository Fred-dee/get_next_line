#include "libft.h"
#include <stdlib.h>

char	*ft_strcpy(char *s1, const char *s2)
{
	size_t	i;

	i = 0;
	while (i < ft_strlen(s2))
	{
		s1[i] = s2[i];
		i++;
	}
	s1[i] = '\0';
	return (s1);
}
