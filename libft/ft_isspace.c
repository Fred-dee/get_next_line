#include "libft.h"

int	ft_isspace(int c)
{
	unsigned char b;

	b = (unsigned char)c;
	if (b == '\t' || b == '\n' || b == '\v' || b == '\f'
		|| b == '\r' || b == ' ')
		return (b);
	return (0);
}
