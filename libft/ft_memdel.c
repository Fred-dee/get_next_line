#include "libft.h"
#include <stdlib.h>
#include <string.h>

void	ft_memdel(void **ap)
{
	if (ap != NULL)
	{
		if (*ap != NULL)
		{
			free(*ap);
			*ap = NULL;
		}
	}
}
